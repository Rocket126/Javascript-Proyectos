//incrementos
//i++ o ++i

let puntajes = 5;

//el incremento es distinto si la variable despues o antes. 
//si la varible va despues del ++, esta variable crecera inmediatamente.
//en cambio con variable++ si la va antes que el ++, la variable crecera despues de imprimirse.

puntajes++;//5 solo crece cuando se desea ver la impresion

++puntajes//6 crece de inmediato.

puntajes -=3;//decrementa de 3 en 3.

console.log(puntajes);//imprime 4

puntajes += 3;//incrementa de 3 en 3;

console.log(puntajes);//7

