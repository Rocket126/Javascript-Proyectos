//usando operadores

let numero1 = 30, numero2 = 20;
let resultado;

//suma
resultado = numero1+numero2;
console.log(resultado);//50

//resta
resultado = numero1-numero2;
console.log(resultado);//10

//Multiplicacion
resultado = numero1*numero2;
console.log(resultado);//600

//Division
resultado = numero1/numero2;
console.log(resultado);//1.5

//modulo (Residuo)
resultado = numero1%numero2;
console.log(resultado);//10